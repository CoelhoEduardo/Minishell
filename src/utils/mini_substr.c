#include "minishell.h"
#include <stdlib.h>

char	*mini_substr(
	t_minishell mini,
	char const *s,
	unsigned int start,
	size_t len
){
	size_t	i;
	size_t	s_len;
	char	*sub;

	i = 0;
	s_len = ft_strlen(s);
	if (start >= s_len)
		return (ft_strdup(""));
	if (s_len - start < len)
		len = s_len - start;
	sub = (char *)mini_alloc((len + 1), sizeof(char));
	if (sub == NULL)
		return (NULL);
	while (i < len)
	{
		sub[i] = s[start + i];
		i++;
	}
	sub[i] = '\0';
	return (sub);
}
