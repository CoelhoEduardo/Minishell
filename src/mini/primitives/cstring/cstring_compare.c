#include "minishell.h"

t_i32	cstring_compare(t_cstring a, t_cstring b)
{
	t_i32	i;

	i = 0;
	while (a[i] == b[i] && a[i] != '\0')
		i++;
	return (((t_u8)a[i]) - ((t_u8)b[i]));
}
